package chapter02변수와자료형;

public class BinaryTest {

	public static void main(String[] args) {
		int num = 10;
		int bNum = 0B1010; // 이진수, 10
		int oNum = 012;    // 8진수
		int hNum = 0XA;    // 16진수
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
